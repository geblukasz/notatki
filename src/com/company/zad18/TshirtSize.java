/*
*
* Fragment kodu powyżej pokazuje typ wyliczeniowy TshirtSize,
* który może mieć jedną z czterech wartości S, M, L lub XL.
* Kolejne wartości typu wyliczeniowego oddzielamy przecinkiem.

Konwencja nadawania nazw zaleca aby wartości dla typu
enum pisane były drukowanymi literami. Zatem TshirtSize.S
jest w porządku, podczas gdy TshirtSize.s już nie.
* */
public enum TshirtSize {

    S,
    M,
    L,
    XL
}
